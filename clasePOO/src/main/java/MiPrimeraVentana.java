    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        this.cliente.setIdCliente(Integer.parseInt(this.idCliente.getText()));
        this.cliente.setNombre(this.nombre.getText());
        this.cliente.setPaterno(this.paterno.getText());
        this.cliente.setMaterno(this.materno.getText());
        this.cliente.setFechaNacimiento(this.fechaNacimiento.getText());
        this.cliente.setNumeroCuenta(Integer.parseInt(this.numeroCuenta.getText()));
        JOptionPane.showMessageDialog(this, "Guardado con Exito...!");
    }//GEN-LAST:event_botonGuardarActionPerformed

